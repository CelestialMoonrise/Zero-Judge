#include <iostream>

using namespace std;

int main()
{
    int a, b, count;
    cin >> a >> b;
    
    count = (b-a+1)/2;
    
    if ((a%2==0) && (b%2==0)){
        count+=1;
    }
    cout << count;

    return 0;
}
